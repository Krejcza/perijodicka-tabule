//KATEGORIE NA VÝBĚR //

 categoryFilter.addEventListener('change', function() {
  const selectedCategory = this.value.toLowerCase()

  const elements = document.querySelectorAll('.element')

  elements.forEach(element => {
    const elementName = element.querySelector('.element-name').textContent

    const tableElement = tableElements.elements.find(el => el.name === elementName)

    if (tableElement) {
      const elementCategory = tableElement.kategorie.toLowerCase()

      if (elementCategory === selectedCategory) {
        element.classList.add('highlight')
      } else {
        element.classList.remove('highlight')
      }
    }
  })

  isradioactive.checked = false
  searchInput.value = ''
  resetTemperatureRange()
})


//VÝBĚR ZADÁNÍM//
 
function filterElements(searchTerm) {
  const elements = document.querySelectorAll('.element')
  
  elements.forEach(element => {
    const elementName = element.getAttribute('title').toLowerCase()
    const elementSymbol = element.querySelector('.symbol').textContent.toLowerCase()
    
    if ((searchTerm && elementName.includes(searchTerm)) || (searchTerm && elementSymbol.includes(searchTerm))) {
      element.classList.add('highlight')
    } else {
      element.classList.remove('highlight')
    }
  })
}


searchInput.addEventListener('input', function() {
  const searchTerm = this.value.trim().toLowerCase();
  filterElements(searchTerm)

  isradioactive.checked = false
  categoryFilter.value = ''
  resetTemperatureRange()
})

//RADIOAKTIVITA//


